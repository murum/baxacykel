        <h1><strong>Spelguide Baxacykel.se</strong></h1>
        <p class="small">
          Baxacykel.se är ett online textbaserat RPG-spel som går ut på att baxa cyklar och investera i bra fabriker för att bli så rik som möjligt. Du kan också tjäna pengar genom att “tradea” varor, vad det innebär är att man flyttar varor från olika städer. Där priset är lågt köper man, där priset är högt säljer man, på så sätt gör du STORA pengar.
        </p>
        <h2>Spela</h2>
        <h3>Baxa</h3>
        <p class="small">
          Här baxar du cyklar som du sedan säljer hos “Handlare”. <br />
          Baxandet av cyklar kommer att ge dig erfarenhetspoäng som gör att du går upp i level och på så sätt låser du upp nya baxobjekt att baxa ifrån. Det finns också andra delar i spelet som öppnar upp sig när du får högre level.
        </p>
        <h3>Handlare</h3>
        <p class="small">
          När du har cyklar i garaget så är det dags att sälja dina cyklar. För att tjäna så mycket pengar som möjligt så bör du spara ihop maximalt antal cyklar som får plats i ditt garage så att du kan sälja till större handlare. Desto fler cyklar du har desto mer pengar har du chans att få ifrån de olika handlarna.  <br />
          Även säljandet av cyklar ger dig erfarenhetspoäng för att gå upp i level.
        </p>
        <h3>Resor</h3>
        <p class="small">
          Resa gör du för att åka till olika marknader för att sälja dina varor som fabrikerna producerar eller som du köper upp på andra marknader. <br />
          Ditt kvarter placerar du i en av städerna. För att plocka upp varor ifrån dina fabriker måste du resa till den stad där du har ditt kvarter.
        </p>
        <h3>Marknad</h3>
        <p class="small">
          På marknaden kan du sälja de varor som dina fabriker producerar. Använd ditt fordon för att flytta varor från ditt lager till marknaden. Res gärna till andra städer för att se om du kan sälja till ett bättre pris där. Priset på marknaden uppdateras varje kvart “.00, .15, .30,  .45” observera att det kan ta några sekunder för priset att uppdateras.
        </p>
        <h3>Kvarteret</h3>
        <p class="small">
          Under kvarteret hanterar du dina ägodelar, du kan uppgraderar ditt garage, aktivera och uppgradera fabriker, uppgradera fordon, se hur mycket varor du har i lager och lasta ditt fordon med dessa.
        </p>
        <h4>Fordon</h4>
        <p class="small">
          Ditt fordon använder du för att frakta varor från lagret och marknaden, du använder också ditt fordon för att tradea varor. Så en större bil bidrar till mer vinst vid trade av varor eftersom du då får med dig fler varor vid varje flytt av varor. Ju större fordon - desto fler varor kan det lasta och desto mer pengar finns det att tjäna.
        </p>
        <h4>Garage</h4>
        <p class="small">
          I ditt garage lagras cyklar som du är ute och baxar. Med ett större Garage så kan du också baxa fler cyklar innan du säljer dem och då kan du också sälja till bättre grossister(handlare).
        </p>
        <h4>Lager</h4>
        <p class="small">
          Lagret används för att lagra varor som kommer direkt från dina fabriker och här kan du också lagra varor från marknaden om du anser att du inte vill tradea varor direkt. <br />
          Varje gång du uppgraderar dina fabriker så kommer varorna som finns i fabriken flyttas till lagret då du inte vill riskera att dina varor går sönder vid ombyggnationen av fabriken.
        </p>
        <h4>Fabriker</h4>
        <p class="small">
          I fabrikerna tillverkas varor kontinuerligt och desto högre nivå du har på din fabrik ju mer varor produceras. Observera att alla varor har olika produktionstider så den varan som har högsta styckpriset behöver inte vara den bästa varan, men vem vet det kanske den är. Det är upp till dig att testa vilken som är den bästa varan. Det som är bäst den här rundan kanske inte är bäst nästa runda. <br />
          Du kan maximalt ha sex stycken fabriker aktiverade samtidigt och aktiveringskostnaden per fabrik är din level multiplicerat med 10.000kr.
        </p>
        <h3>Skolan</h3>
        <p class="small">
          I skolan så kan du öka dina skills, observera dock att dessa har längre väntetider så ifall du har tänkt spela aktivt så kommer du bli låst och inte kan baxa under tiden som du är i skolan. Men vem har sagt att det är mer värt att baxa, kanske är det skolan som gäller för att vinna? Självklart är det en mix av allt som bidrar till vinsterna.
        </p>
        <h4>Rörlighet</h4>
        <p class="small">
          Att ha en högre rörlighet minskar väntetiden i både skolan och baxandet. <br />
          En poäng i rörlighet innebär 0.5% minskad väntetid. Du kan max ha 80 poäng i rörlighet och därmed minska väntetiden med 40%.
        </p>
        <h4>Intelligens</h4>
        <p class="small">
          Intelligens gör dig till en bättre säljare, du får alltså mer pengar för varje cykel du säljer hos handlarna. En intelligens innebär 2% högre pris per såld cykel. Här finns det ingen maxgräns utan du kan träna intelligens till hjärnan växer ut ur huvudet.
        </p>
        <h3>Klubb</h3>
        <p class="small">
